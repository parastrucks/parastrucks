const XLSX = require('xlsx')
const fs   = require('fs')
const path = require('path')

const EXCEL = 'D:/PTB/Website/parastrucks/pricelist/AL_Vehicle_Price_List_Apr2026.xlsx'
const OUT   = path.join(__dirname, '_out')

if (!fs.existsSync(OUT)) fs.mkdirSync(OUT)

// ── Read Excel ────────────────────────────────────────────────────────────────
const wb     = XLSX.readFile(EXCEL)
const ws     = wb.Sheets['Vehicle Price List']
const allRows = XLSX.utils.sheet_to_json(ws, { header: 1 }).slice(1)
  .filter(r => r[2] && r[5])
  .map(r => ({
    segment:    String(r[0] || '').trim(),
    sub_cat:    String(r[1] || '').trim(),
    cbn:        String(r[2] || '').trim(),
    desc:       String(r[3] || '').trim(),
    tyres:      String(r[4] || '').trim(),
    mrp:        parseInt(String(r[5]).replace(/[^0-9]/g, ''), 10),
  }))
  .filter(r => r.mrp > 0)

// ── Sep 2025 CBNs (from old seed) — needed to compute deactivation list ───────
// We read the old seed SQL to extract the full CBN list rather than hardcoding
const SEED_SQL = path.join(__dirname, '..', 'seed_vehicles.sql')
const oldCBNs  = new Set()
if (fs.existsSync(SEED_SQL)) {
  const txt = fs.readFileSync(SEED_SQL, 'utf8')
  for (const m of txt.matchAll(/'([A-Z0-9_]+)',\s*'[^']+',\s*'[^']+'/g)) {
    oldCBNs.add(m[1])
  }
}
const newCBNSet = new Set(allRows.map(r => r.cbn))
const deactivate = [...oldCBNs].filter(c => !newCBNSet.has(c))
process.stderr.write(`Old CBNs: ${oldCBNs.size} | Apr 2026: ${allRows.length} | To deactivate: ${deactivate.length}\n`)

// ── A) SQL Migration ──────────────────────────────────────────────────────────
const esc = s => String(s ?? '').replace(/'/g, "''").trim()

function priceCircular(seg) {
  if (seg === 'Bus – ICV' || seg === 'Bus – MCV') return 'PC-155 (Apr 2026)'
  return 'Apr 2026 Circular'
}

const sqlLines = [
  '-- Vehicle catalog update — Ashok Leyland Apr 2026 price list',
  '-- Source: AL_Vehicle_Price_List_Apr2026.xlsx (effective 2026-04-01)',
  '-- Generated by scripts/apply_apr2026_prices.cjs',
  '-- DO NOT manually edit — regenerate from script',
  '',
  '-- Upsert 797 active CBNs with Apr 2026 MRP values',
  'INSERT INTO public.vehicle_catalog',
  '  (cbn, description, segment, sub_category, tyres, mrp_incl_gst, gst_rate, price_circular, effective_date, is_active)',
  'VALUES',
]

const sqlValues = allRows.map(r => {
  const tyresVal = (r.tyres && r.tyres !== r.desc) ? `'${esc(r.tyres)}'` : 'NULL'
  return `  ('${esc(r.cbn)}', '${esc(r.desc)}', '${esc(r.segment)}', '${esc(r.sub_cat)}', ` +
         `${tyresVal}, ${r.mrp}, 18, '${priceCircular(r.segment)}', '2026-04-01', true)`
})
sqlLines.push(sqlValues.join(',\n'))
sqlLines.push('ON CONFLICT (cbn) DO UPDATE SET')
sqlLines.push('  description     = EXCLUDED.description,')
sqlLines.push('  segment         = EXCLUDED.segment,')
sqlLines.push('  sub_category    = EXCLUDED.sub_category,')
sqlLines.push('  tyres           = EXCLUDED.tyres,')
sqlLines.push('  mrp_incl_gst    = EXCLUDED.mrp_incl_gst,')
sqlLines.push('  price_circular  = EXCLUDED.price_circular,')
sqlLines.push('  effective_date  = EXCLUDED.effective_date,')
sqlLines.push('  is_active       = true;')
sqlLines.push('')

if (deactivate.length > 0) {
  sqlLines.push('-- Deactivate discontinued CBNs (not in Apr 2026 price list)')
  sqlLines.push(`UPDATE public.vehicle_catalog SET is_active = false`)
  sqlLines.push(`WHERE cbn IN (`)
  // chunk into rows of 5
  for (let i = 0; i < deactivate.length; i += 5) {
    const chunk = deactivate.slice(i, i + 5).map(c => `  '${c}'`).join(', ')
    sqlLines.push(chunk + (i + 5 < deactivate.length ? ',' : ''))
  }
  sqlLines.push(');')
  sqlLines.push('')
}

sqlLines.push(`-- ${allRows.length} rows upserted, ${deactivate.length} CBNs deactivated`)

const sqlPath = path.join(__dirname, '..', 'supabase', 'migrations', '20260401_apr2026_prices.sql')
fs.mkdirSync(path.dirname(sqlPath), { recursive: true })
fs.writeFileSync(sqlPath, sqlLines.join('\n') + '\n')
process.stderr.write(`✓ SQL → ${sqlPath}\n`)

