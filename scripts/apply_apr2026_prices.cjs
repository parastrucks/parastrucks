// Format: {s, m, p} — segment display name, model name, min MRP in sub_category
// EMI calc shows segment groups: Buses, Haulage, ICV Trucks, Tippers

const segMap = {
  'Bus – ICV':      'Buses',
  'Bus – MCV':      'Buses',
  'ICV Truck':      'ICV Trucks',
  'MBP Truck':      'Haulage',
  'Tipper':         'Tippers',
  'RMC / Boom Pump':'Tippers',
}

// Group by (display_segment, sub_cat) → min MRP
const emiMap = {}
allRows.forEach(r => {
  const s = segMap[r.segment]
  if (!s) return
  // Use sub_cat as model name
  const key = `${s}__${r.sub_cat}`
  if (!emiMap[key] || r.mrp < emiMap[key].p) {
    emiMap[key] = { s, m: r.sub_cat, p: r.mrp }
  }
})

// Sort by segment order then model name
const SEG_ORDER = ['Buses', 'ICV Trucks', 'Haulage', 'Tippers']
const emiRows = Object.values(emiMap).sort((a, b) => {
  const si = SEG_ORDER.indexOf(a.s) - SEG_ORDER.indexOf(b.s)
  return si !== 0 ? si : a.m.localeCompare(b.m)
})

